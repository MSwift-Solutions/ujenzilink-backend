package com.ujenzilink.ujenzilink_backend.projects.enums;

public enum ConstructionStage {
    PLANNING_PERMITS,          // Design, blueprints, and legal approvals
    SITE_CLEARING_EXCAVATION,  // Clearing land and digging
    FOUNDATION_WORK,           // Pouring the concrete base
    STRUCTURAL_FRAMEWORK,      // The "skeleton" (walls and floors)
    ROOFING_ENCLOSURE,         // Making it weatherproof (windows/roof)
    PLUMBING_ELECTRICAL,       // Internal "guts" (pipes and wiring)
    WALLS_CEILINGS,            // Drywall, insulation, and plaster
    INTERIOR_FINISHING,        // Paint, flooring, and cabinets
    EXTERIOR_LANDSCAPING,      // Yard, driveway, and final cleanup
    FINAL_INSPECTION_HANDOVER   // Final walkthrough and keys
}
